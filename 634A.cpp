#include <bits/stdc++.h>

using namespace std;

int main()
{
   int n,cnta=0,cntd=0;
   cin>>n;
   for(int i=0 ; i<n ;i++)
   {
        char x;
        cin>>x;
        if(x=='A') cnta++;
        else cntd++;
    }
    if(cnta>cntd)
    cout<<"Anton"<<endl;
    else if (cntd>cnta)
        cout<<"Danik"<<endl;
    else if (cnta==cntd) cout<<"Friendship"<<endl;
       return 0;
}
